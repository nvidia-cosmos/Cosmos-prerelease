# EE frame name candidates (tried in order by IK solver)
# ---------------------------------------------------------------------------
# End-effector frame references per dataset
# ---------------------------------------------------------------------------
# Each dataset records EE poses at a specific URDF link.  The IK solver must
# target the same frame (via ``ee_frame`` or the ``_EE_FRAME_CANDIDATES``
# fallback list).  Below we document, for every robot:
#   (a) which link the dataset records at, with upstream source reference,
#   (b) what that link physically represents (flange, gripper body, or TCP),
#   (c) any orientation transforms applied during recording.
#
# ── Google Robot (fractal / RT-1) ──────────────────────────────────────────
#   Recorded link: ``link_gripper_tcp``
#     The TFDS observation field is ``base_pose_tool_reached`` (shape (7,)).
#     Source: https://www.tensorflow.org/datasets/catalog/fractal20220817_data
#   Meaning: TCP — a calibrated tool-center-point 164 mm past the wrist
#     (``link_gripper``), roughly at the fingertip.  The name contains "tool"
#     and SimplerEnv's real→sim calibration targets the same link:
#       ``self.ee_link_name = "link_gripper_tcp"``
#     Source: https://github.com/simpler-env/ManiSkill2_real2sim/blob/ef7a4d4/mani_skill2_real2sim/agents/configs/google_robot/defaults.py#L141
#   Orientation: recorded as-is from the URDF FK (quaternion). No extra
#     rotation applied.
#   Re-referenced to: ``link_gripper`` (gripper body / last actuated link)
#     via ``_TCP_TO_FLANGE`` in the dataset class (−164 mm along local z).
#     See ``fractal.py`` for the full transform and rationale.
#   Our config: ``"ee_frame": "link_gripper"`` (explicit).
#   Important MJCF note: MuJoCo Menagerie places ``base_link`` at
#     ``pos="0 0 0.06205"`` under ``worldbody``. Pinocchio's
#     ``buildModelFromMJCF()`` omits this global root transform, so IK / dataset
#     poses live in a root-free world that is 62.05 mm lower than raw MuJoCo
#     body/site poses. We therefore strip the same root transform from MuJoCo
#     mesh/body/site outputs before rendering or comparing them.
#
# ── WidowX (bridge) ───────────────────────────────────────────────────────
#   Recorded link: ``ee_gripper_link``
#     The bridge data collection uses the Interbotix SDK's Modern Robotics FK
#     (``FKinSpace(M, Slist, q)``).  The M matrix for wx250s terminates at
#     ``ee_gripper_link`` — confirmed by the Interbotix source comment and
#     numerically: ``wx250s.M[0,3] = 0.458325`` matches ``ee_gripper_link``
#     position exactly (vs ``gripper_link`` at 0.3648).
#     Source: https://github.com/Interbotix/interbotix_ros_toolboxes/blob/main/interbotix_xs_toolbox/interbotix_xs_modules/src/interbotix_xs_modules/mr_descriptions.py
#       Line 1: "Note that the end-effector is positioned at '<robot_name>/ee_gripper_link'"
#     SimplerEnv confirms: ``self.ee_link_name = "ee_gripper_link"``
#     Source: https://github.com/simpler-env/ManiSkill2_real2sim/blob/ef7a4d4/mani_skill2_real2sim/agents/configs/widowx/defaults.py#L87
#   Meaning: Interbotix-defined "end-effector" reference point, 93.6 mm past
#     the wrist (``gripper_link``), 27.6 mm past the finger pivot
#     (``fingers_link``).  This is roughly the grasp center between the
#     finger pads — *not* the fingertip.
#   Orientation: the bridge data collection applies ``DEFAULT_ROTATION`` to
#     the FK orientation before recording (in ``transform2state``):
#       ``euler = rotationMatrixToEulerAngles(rot.dot(default_rotation.T))``
#     where ``DEFAULT_ROTATION = [[0,0,1],[0,1,0],[-1,0,0]]``.
#     Source: https://github.com/rail-berkeley/bridge_data_robot/blob/main/widowx_envs/widowx_controller/src/widowx_controller/widowx_controller.py#L44
#   Re-referenced to: ``gripper_link`` (wrist rotate body / last actuated link)
#     via ``_TCP_TO_FLANGE`` in the dataset class (−93.6 mm along local x).
#     See ``bridge_orig_lerobot_dataset.py`` for the full transform and rationale.
#   Our config: ``"ee_frame": "gripper_link"`` (explicit).
#
# ── Franka + Robotiq 2F-85 (DROID) ────────────────────────────────────────
#   Recorded link: ``panda_link8`` (= Franka flange)
#     DROID's ``get_robot_state()`` calls Polymetis FK:
#       ``pos, quat = self._robot.robot_model.forward_kinematics(joint_positions)``
#     Source: https://github.com/droid-dataset/droid/blob/main/droid/franka/robot.py
#     Polymetis is configured with ``ee_link_name: panda_link8``:
#     Source: https://github.com/facebookresearch/fairo/blob/main/polymetis/polymetis/conf/robot_model/franka_panda.yaml
#   Meaning: flange — the bare mounting plate at the end of the Franka arm,
#     *before* the Robotiq gripper.
#   MJCF body: ``panda_hand`` in the composite model (pos 0 0 0.107
#     above ``link7``, identity quat).  This is at the panda_link8
#     (flange) position with link7 orientation, matching the URDF.
#     ``link7`` is panda_link7 (wrist), *not* the flange.
#   Orientation: Polymetis reports FK orientation at ``panda_link8`` frame
#     as-is. No extra rotation applied.
#   Current viewer convention: all Franka-backed datasets reuse this DROID
#     composite model, so ``franka_panda`` is the only supported Franka robot
#     config in this module.
#   Our config: ``"ee_frame": "panda_hand"`` (explicit).
# ---------------------------------------------------------------------------

