def _get_visual_geom_ids(model) -> list[int]:
    """Get visual geom IDs from a MuJoCo model.

    Strategy:
    1. Google Robot uses *_v suffix for visual meshes → use those
    2. Other robots (Franka, WidowX) use geom_group=2 for visual → use those
    3. Fallback: all mesh geoms

    Includes both mesh geoms and primitive geoms (box, cylinder, sphere) in group 2.
    """
    import mujoco

    VISUAL_TYPES = {
        mujoco.mjtGeom.mjGEOM_MESH,
        mujoco.mjtGeom.mjGEOM_BOX,
        mujoco.mjtGeom.mjGEOM_CYLINDER,
        mujoco.mjtGeom.mjGEOM_SPHERE,
        mujoco.mjtGeom.mjGEOM_CAPSULE,
        mujoco.mjtGeom.mjGEOM_ELLIPSOID,
    }

    v_ids = []
    group2_ids = []
    all_ids = []
    for gi in range(model.ngeom):
        gtype = model.geom_type[gi]
        if gtype not in VISUAL_TYPES:
            continue
        all_ids.append(gi)
        if gtype == mujoco.mjtGeom.mjGEOM_MESH:
            mesh_id = model.geom_dataid[gi]
            mesh_name = mujoco.mj_id2name(model, mujoco.mjtObj.mjOBJ_MESH, mesh_id) or ""
            if mesh_name.endswith("_v"):
                v_ids.append(gi)
        if model.geom_group[gi] == 2:
            group2_ids.append(gi)

    # Priority: _v suffix > group 2 > all
    if v_ids:
        return v_ids
    if group2_ids:
        return group2_ids
    return all_ids


def _resolve_geom_color(model, gi: int, geom_name: str) -> np.ndarray:
    """Resolve the effective RGBA color for a MuJoCo geom.

    Priority:
    1. If geom has a material with a diffuse texture → sample average texture color
    2. If geom has a material with non-default rgba → use mat_rgba
    3. If geom_rgba is non-default → use geom_rgba
    4. Fallback: deterministic per-geom color

    Returns (4,) uint8 RGBA.
    """

    rgba = model.geom_rgba[gi].copy()  # (4,) float [0,1]
    mat_id = model.geom_matid[gi]

    if mat_id >= 0:
        # Check if material has a diffuse texture
        tex_id = model.mat_texid[mat_id][1]  # index 1 = diffuse texture
        if tex_id >= 0:
            # Sample average color from the texture
            tex_adr = model.tex_adr[tex_id]
            tex_w = model.tex_width[tex_id]
            tex_h = model.tex_height[tex_id]
            n_pixels = tex_w * tex_h
            tex_data = model.tex_data[tex_adr : tex_adr + n_pixels * 3].reshape(n_pixels, 3)
            avg_rgb = tex_data.mean(axis=0) / 255.0
            # Multiply by mat_rgba (which acts as a tint)
            mat_rgba = model.mat_rgba[mat_id]
            final = np.array(
                [
                    avg_rgb[0] * mat_rgba[0],
                    avg_rgb[1] * mat_rgba[1],
                    avg_rgb[2] * mat_rgba[2],
                    mat_rgba[3],
                ]
            )
            return (np.clip(final, 0, 1) * 255).astype(np.uint8)

        # No texture — use material rgba if it is informative.
        mat_rgba = model.mat_rgba[mat_id].copy()
        if not _is_unspecified_rgba(mat_rgba):
            return (np.clip(mat_rgba, 0, 1) * 255).astype(np.uint8)

    if not _is_unspecified_rgba(rgba):
        return (np.clip(rgba, 0, 1) * 255).astype(np.uint8)

    return _fallback_mesh_color(geom_name, gi)


def _extract_mujoco_meshes(model, data) -> list:
    """Extract visual geom meshes from a MuJoCo model as trimesh objects.

    Uses the same visual geom filtering as ``ik_solver._get_visual_geom_ids``
    to ensure mesh ordering matches IK transform output.

    For geoms with primitive types (box, cylinder, etc.), generates the
    corresponding trimesh primitive.

    Applies per-geom RGBA color from MuJoCo materials when available.

    Returns list of (geom_name, trimesh.Trimesh, 4x4_transform).
    """
    import mujoco
    import trimesh

    # Use the same visual filter as IK solver
    visual_geom_ids = _get_visual_geom_ids(model)

    meshes = []
    for gi in visual_geom_ids:
        gtype = model.geom_type[gi]
        pos = data.geom_xpos[gi].copy()
        rot = data.geom_xmat[gi].reshape(3, 3).copy()

        geom_name = mujoco.mj_id2name(model, mujoco.mjtObj.mjOBJ_GEOM, gi) or f"geom_{gi}"
        face_color = _resolve_geom_color(model, gi, geom_name)

        mesh = None
        if gtype == mujoco.mjtGeom.mjGEOM_MESH:
            mesh_id = model.geom_dataid[gi]
            vert_start = model.mesh_vertadr[mesh_id]
            vert_count = model.mesh_vertnum[mesh_id]
            face_start = model.mesh_faceadr[mesh_id]
            face_count = model.mesh_facenum[mesh_id]
            verts = model.mesh_vert[vert_start : vert_start + vert_count].copy()
            faces = model.mesh_face[face_start : face_start + face_count].copy()
            mesh = trimesh.Trimesh(vertices=verts, faces=faces)
            _apply_mesh_color(mesh, face_color)
        elif gtype == mujoco.mjtGeom.mjGEOM_BOX:
            size = model.geom_size[gi].copy()
            mesh = trimesh.creation.box(extents=size * 2)
            _apply_mesh_color(mesh, face_color)
        elif gtype == mujoco.mjtGeom.mjGEOM_SPHERE:
            mesh = trimesh.creation.icosphere(radius=model.geom_size[gi][0])
            _apply_mesh_color(mesh, face_color)
        elif gtype == mujoco.mjtGeom.mjGEOM_CYLINDER:
            mesh = trimesh.creation.cylinder(radius=model.geom_size[gi][0], height=model.geom_size[gi][1] * 2)
            _apply_mesh_color(mesh, face_color)
        elif gtype == mujoco.mjtGeom.mjGEOM_CAPSULE:
            mesh = trimesh.creation.capsule(radius=model.geom_size[gi][0], height=model.geom_size[gi][1] * 2)
            _apply_mesh_color(mesh, face_color)

        if mesh is not None:
            transform = np.eye(4)
            transform[:3, :3] = rot
            transform[:3, 3] = pos
            meshes.append((geom_name, mesh, transform))
    return meshes


def get_mujoco_to_pinocchio_world_transform(model, data, robot_name: str | None = None) -> np.ndarray:
    """Return a correction that maps MuJoCo world poses into Pinocchio world.

    For some MJCFs, MuJoCo includes a fixed worldbody -> root-body transform
    that Pinocchio's ``buildModelFromMJCF()`` omits. When that happens, the IK
    solver and dataset poses live in a root-free world, while raw MuJoCo
    body/site poses are globally shifted. This helper returns the inverse root
    transform so callers can strip that offset from MuJoCo-derived meshes,
    body frames, and sites before comparing them against Pinocchio / dataset
    poses.
    """
    import mujoco

    cfg = ROBOT_CONFIGS.get(robot_name, {}) if robot_name else {}
    root_body_name = cfg.get("pinocchio_removed_root_body")
    if not root_body_name:
        return np.eye(4, dtype=np.float32)

    root_body_id = mujoco.mj_name2id(model, mujoco.mjtObj.mjOBJ_BODY, root_body_name)
    if root_body_id < 0:
        log.warning(f"Configured root body '{root_body_name}' not found in MuJoCo model")
        return np.eye(4, dtype=np.float32)

    root_pose = np.eye(4, dtype=np.float32)
    root_pose[:3, 3] = data.xpos[root_body_id].astype(np.float32)
    root_pose[:3, :3] = data.xmat[root_body_id].reshape(3, 3).astype(np.float32)
    return np.linalg.inv(root_pose).astype(np.float32)


def _apply_world_transform_to_meshes(
    meshes: list[tuple[str, object, np.ndarray]],
    world_transform: np.ndarray,
) -> list[tuple[str, object, np.ndarray]]:
    """Left-multiply a world-space transform into each mesh pose."""
    transformed_meshes = []
    for name, mesh, transform in meshes:
        transformed_meshes.append((name, mesh, (world_transform @ transform).astype(np.float32)))
    return transformed_meshes


