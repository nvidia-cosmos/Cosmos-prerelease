def get_urdf_path(robot_name: str) -> str | None:
    """Get URDF path for a robot if available, else None (falls back to MJCF in caller)."""
    cfg = ROBOT_CONFIGS.get(robot_name)
    if cfg is None:
        return None

    if robot_name == "embodiment_c":
        return str(get_embodiment_c_urdf_path())

    menagerie_name = cfg.get("menagerie")
    if menagerie_name is None:
        return None
    mjcf_dir = _ensure_robot_assets(menagerie_name)

    urdf_filename = cfg.get("urdf")
    if urdf_filename:
        urdf_path = mjcf_dir / urdf_filename
        if urdf_path.exists():
            return str(urdf_path)

    urdfs = list(mjcf_dir.glob("*.urdf"))
    if urdfs:
        return str(urdfs[0])

    return None


def get_mjcf_path(robot_name: str) -> str:
    """Get MJCF path for a robot, downloading from mujoco_menagerie if needed."""
    cfg = ROBOT_CONFIGS.get(robot_name)
    if cfg is None:
        raise ValueError(f"Unknown robot: {robot_name}. Available: {list(ROBOT_CONFIGS.keys())}")

    menagerie_name = cfg.get("menagerie")
    if menagerie_name is None:
        raise FileNotFoundError(f"Robot {robot_name!r} does not have a MuJoCo MJCF asset.")
    mjcf_filename = cfg.get("mjcf", "robot.xml")

    mjcf_dir = _ensure_robot_assets(menagerie_name)
    mjcf_path = mjcf_dir / mjcf_filename
    if mjcf_path.exists():
        return str(mjcf_path)

    fallback = mjcf_dir / "robot.xml"
    if fallback.exists():
        return str(fallback)

    raise FileNotFoundError(f"MJCF not found: {mjcf_path} (also tried robot.xml)")


# ── Asset download ───────────────────────────────────────────────────────────


def _ensure_robot_assets(robot_name: str) -> Path:
    """Ensure robot MJCF and mesh assets are available locally.

    Downloads from mujoco_menagerie GitHub repo if not found.
    For composite robots (``droid_franka_robotiq``), downloads from both
    MuJoCo Menagerie and MuJoCo Playground and merges assets.

    Assets are cached in ~/.cache/mujoco_menagerie/<robot_name>/.

    Returns:
        Path to the cached MJCF directory (caller picks the right .xml).
    """
    cached_dir = _ROBOT_CACHE_DIR / robot_name
    if cached_dir.exists():
        return cached_dir

    # Composite: Franka arm + Robotiq 2F-85 gripper (DROID setup)
    if robot_name == "droid_franka_robotiq":
        return _build_droid_franka_robotiq()

    # Composite: UR5e arm + Robotiq 2F-85 gripper (RoboMIND UR setup)
    if robot_name == "ur5e_robotiq":
        return _build_ur5e_robotiq()

    log.info(f"Downloading {robot_name} assets from mujoco_menagerie...")
    _download_menagerie_model(robot_name)
    return cached_dir


def _download_menagerie_model(model_name: str) -> Path:
    """Download a single model from MuJoCo Menagerie. Returns cached path."""
    cached_dir = _ROBOT_CACHE_DIR / model_name
    if cached_dir.exists():
        return cached_dir

    import shutil
    import subprocess
    import tempfile

    _ROBOT_CACHE_DIR.mkdir(parents=True, exist_ok=True)
    with tempfile.TemporaryDirectory() as tmpdir:
        subprocess.run(
            ["git", "clone", "--depth=1", "--filter=blob:none", "--sparse", _MENAGERIE_REPO, tmpdir],
            check=True,
            capture_output=True,
        )
        subprocess.run(
            ["git", "sparse-checkout", "set", model_name],
            cwd=tmpdir,
            check=True,
            capture_output=True,
        )
        src = Path(tmpdir) / model_name
        dst = _ROBOT_CACHE_DIR / model_name
        if dst.exists():
            shutil.rmtree(dst)
        shutil.copytree(src, dst)

    log.info(f"Cached {model_name} assets at {dst}")
    return dst


def _build_droid_franka_robotiq() -> Path:
    """Prepare composite Franka + Robotiq 2F-85 MJCF for DROID visualization.

    The hand-tuned composite XML is committed in the repo alongside this
    module (``droid_franka_robotiq_2f85.xml``).  This function downloads the
    mesh assets from MuJoCo Menagerie (``franka_emika_panda`` +
    ``robotiq_2f85_v4``) and copies them together with the XML into a cache
    directory that MuJoCo can load from.
    """
    import shutil

    log.info("Preparing composite Franka + Robotiq 2F-85 (committed XML + Menagerie assets)...")

    franka_dir = _download_menagerie_model("franka_emika_panda")
    robotiq_dir = _download_menagerie_model("robotiq_2f85_v4")

    dst = _ROBOT_CACHE_DIR / "droid_franka_robotiq"
    dst.mkdir(parents=True, exist_ok=True)
    assets_dir = dst / "assets"
    assets_dir.mkdir(exist_ok=True)

    for f in (franka_dir / "assets").iterdir():
        shutil.copy2(f, assets_dir / f.name)
    for f in (robotiq_dir / "assets").iterdir():
        shutil.copy2(f, assets_dir / f.name)

    committed_xml = Path(__file__).parent / "droid_franka_robotiq_2f85.xml"
    out_path = dst / "panda_updated_robotiq_2f85.xml"
    shutil.copy2(committed_xml, out_path)

    log.info(f"Prepared composite Franka + Robotiq 2F-85 at {dst}")
    return dst


