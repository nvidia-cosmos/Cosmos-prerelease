def _fallback_mesh_color(name: str, index: int = 0) -> np.ndarray:
    """Return a stable fallback RGBA color for a mesh without material color."""

    key = name or f"mesh_{index}"
    palette_index = sum((char_idx + 1) * ord(char) for char_idx, char in enumerate(key))
    return _FALLBACK_MESH_COLORS[(palette_index + index) % len(_FALLBACK_MESH_COLORS)].copy()


def _is_unspecified_rgba(rgba: np.ndarray) -> bool:
    """Return whether RGBA is an uninformative default color."""

    return any(np.allclose(rgba, default_rgba, atol=1e-4) for default_rgba in _UNSPECIFIED_RGBA_VALUES)


def _apply_mesh_color(mesh: Any, rgba: np.ndarray) -> None:
    """Apply one RGBA color to all mesh faces for viser/trimesh rendering."""

    face_count = int(len(getattr(mesh, "faces", [])))
    if face_count > 0:
        mesh.visual.face_colors = np.tile(np.asarray(rgba, dtype=np.uint8), (face_count, 1))


