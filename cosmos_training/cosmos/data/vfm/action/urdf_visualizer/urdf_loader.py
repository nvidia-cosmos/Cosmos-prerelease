def _parse_urdf_origin(origin_element) -> np.ndarray:
    transform = np.eye(4, dtype=np.float32)
    if origin_element is None:
        return transform

    xyz_text = origin_element.attrib.get("xyz", "0 0 0")
    rpy_text = origin_element.attrib.get("rpy", "0 0 0")
    xyz = np.asarray([float(value) for value in xyz_text.split()], dtype=np.float32)
    rpy = np.asarray([float(value) for value in rpy_text.split()], dtype=np.float32)
    transform[:3, :3] = R.from_euler("xyz", rpy).as_matrix().astype(np.float32)
    transform[:3, 3] = xyz
    return transform


def _parse_urdf_color(color_element: ET.Element | None) -> np.ndarray | None:
    if color_element is None:
        return None
    rgba_text = color_element.attrib.get("rgba")
    if not rgba_text:
        return None
    rgba = np.asarray([float(value) for value in rgba_text.split()], dtype=np.float32)
    if rgba.shape != (4,):
        return None
    return (np.clip(rgba, 0.0, 1.0) * 255.0).astype(np.uint8)


def _parse_urdf_material_colors(root: ET.Element) -> dict[str, np.ndarray]:
    material_colors: dict[str, np.ndarray] = {}
    for material_element in root.findall("material"):
        material_name = material_element.attrib.get("name", "")
        color = _parse_urdf_color(material_element.find("color"))
        if material_name and color is not None:
            material_colors[material_name] = color
    return material_colors


def _resolve_urdf_visual_color(parent_element: ET.Element, material_colors: dict[str, np.ndarray]) -> np.ndarray | None:
    material_element = parent_element.find("material")
    if material_element is None:
        return None

    material_name = material_element.attrib.get("name", "")
    color = _parse_urdf_color(material_element.find("color"))
    if color is None and material_name:
        color = material_colors.get(material_name)
    if color is None:
        return None

    color_unit = color.astype(np.float32) / 255.0
    if material_name.lower() in _UNSPECIFIED_MATERIAL_NAMES or _is_unspecified_rgba(color_unit):
        return None
    return color


def _agibot_urdf_asset_roots() -> list[Path]:
    """Return candidate roots that can satisfy package://genie_robot_description assets."""

    roots: list[Path] = []
    env_urdf_root = os.environ.get("AGIBOT_GEAR_URDF_ROOT")
    if env_urdf_root:
        roots.append(Path(env_urdf_root))
    env_mesh_root = os.environ.get("AGIBOT_GEAR_MESH_ROOT")
    if env_mesh_root:
        roots.append(Path(env_mesh_root))
    roots.extend(
        [
            get_embodiment_c_urdf_path().parent,
            _AGIBOT_GEAR_LUSTRE_URDF_ROOT,
            _AGIBOT_GEAR_LOCAL_SAMPLE_URDF_ROOT,
        ]
    )

    unique_roots: list[Path] = []
    seen: set[Path] = set()
    for root in roots:
        resolved = root.expanduser()
        if resolved in seen:
            continue
        seen.add(resolved)
        unique_roots.append(resolved)
    return unique_roots


def _resolve_agibot_mesh_path(filename: str) -> Path:
    if filename.startswith("package://genie_robot_description/"):
        rel = filename.removeprefix("package://genie_robot_description/")
        candidates: list[Path] = []
        for root in _agibot_urdf_asset_roots():
            candidates.append(root / rel)
            if rel.startswith("meshes/"):
                candidates.append(root / rel.removeprefix("meshes/"))
        for candidate in candidates:
            if candidate.is_file():
                return candidate
        return candidates[0]
    path = Path(filename)
    if path.is_absolute():
        return path
    return get_embodiment_c_urdf_path().parent / path


def _build_urdf_geometry_mesh(geometry_element: ET.Element | None, warn_missing_mesh: bool = False) -> Any | None:
    import trimesh

    if geometry_element is None:
        return None

    mesh_element = geometry_element.find("mesh")
    if mesh_element is not None:
        filename = mesh_element.attrib.get("filename")
        if not filename:
            return None
        mesh_path = _resolve_agibot_mesh_path(filename)
        if not mesh_path.is_file():
            if warn_missing_mesh:
                log.warning(f"AgiBot mesh file missing: {mesh_path}")
            return None
        try:
            mesh = trimesh.load(mesh_path, force="mesh")
            if hasattr(mesh, "geometry"):
                mesh = trimesh.util.concatenate(tuple(mesh.geometry.values()))
        except Exception as exc:
            log.warning(f"Failed to load AgiBot mesh {mesh_path}: {exc}")
            return None
        scale_text = mesh_element.attrib.get("scale")
        if scale_text:
            mesh.apply_scale([float(value) for value in scale_text.split()])
        return mesh

    box = geometry_element.find("box")
    if box is not None:
        size = np.asarray([float(value) for value in box.attrib["size"].split()], dtype=np.float32)
        return trimesh.creation.box(extents=size)

    sphere = geometry_element.find("sphere")
    if sphere is not None:
        radius = float(sphere.attrib["radius"])
        return trimesh.creation.icosphere(radius=radius)

    cylinder = geometry_element.find("cylinder")
    if cylinder is not None:
        radius = float(cylinder.attrib["radius"])
        length = float(cylinder.attrib["length"])
        return trimesh.creation.cylinder(radius=radius, height=length)

    return None


@lru_cache(maxsize=1)
def _load_embodiment_c_collision_geometries() -> tuple[AgibotGeometry, ...]:
    root = ET.parse(get_embodiment_c_urdf_path()).getroot()
    material_colors = _parse_urdf_material_colors(root)
    geometries: list[AgibotGeometry] = []

    for link_element in root.findall("link"):
        link_name = link_element.attrib["name"]
        visual_elements = link_element.findall("visual")
        collision_elements = link_element.findall("collision")
        # Prefer visual meshes when the real asset files are available. Collision
        # primitives remain the fallback for environments without AgiBot mesh assets.
        for elements, warn_missing_meshes in ((visual_elements, False), (collision_elements, False)):
            loaded_before = len(geometries)
            for geometry_idx, parent_element in enumerate(elements):
                geometry_element = parent_element.find("geometry")
                mesh = _build_urdf_geometry_mesh(geometry_element, warn_missing_mesh=warn_missing_meshes)
                if mesh is None:
                    continue
                geometry_name = f"{link_name}_geometry_{geometry_idx}"
                face_color = _resolve_urdf_visual_color(parent_element, material_colors)
                if face_color is None:
                    face_color = _fallback_mesh_color(geometry_name, geometry_idx)
                _apply_mesh_color(mesh, face_color)
                local_transform = _parse_urdf_origin(parent_element.find("origin"))
                geometries.append(
                    AgibotGeometry(
                        name=geometry_name,
                        link_name=link_name,
                        mesh=mesh,
                        local_transform=local_transform,
                    )
                )
            if len(geometries) > loaded_before:
                break

    return tuple(geometries)


def get_embodiment_c_collision_geometries() -> list[AgibotGeometry]:
    """Return cached AgiBot primitive collision geometries from the committed G1 URDF."""

    return list(_load_embodiment_c_collision_geometries())


