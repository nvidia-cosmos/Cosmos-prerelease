def _load_google_robot() -> tuple[list, np.ndarray]:
    """Load Google Robot from MuJoCo Menagerie.

    Uses the official google_robot model from
    https://github.com/google-deepmind/mujoco_menagerie/tree/main/google_robot
    which has visual OBJ meshes for accurate rendering.
    """
    import mujoco

    cfg = ROBOT_CONFIGS["google_robot"]
    mjcf_dir = _ensure_robot_assets(cfg["menagerie"])
    mjcf_path = mjcf_dir / cfg["mjcf"]
    model = mujoco.MjModel.from_xml_path(str(mjcf_path))
    data = mujoco.MjData(model)
    mujoco.mj_forward(model, data)

    meshes = _extract_mujoco_meshes(model, data)
    world_correction = get_mujoco_to_pinocchio_world_transform(model, data, "google_robot")
    if not np.allclose(world_correction, np.eye(4, dtype=np.float32)):
        meshes = _apply_world_transform_to_meshes(meshes, world_correction)

    # Find EE pose at link_gripper
    ee_pose = np.eye(4, dtype=np.float32)
    for i in range(model.nbody):
        name = mujoco.mj_id2name(model, mujoco.mjtObj.mjOBJ_BODY, i) or ""
        if name == "link_gripper":
            ee_pose[:3, 3] = data.xpos[i].astype(np.float32)
            ee_pose[:3, :3] = data.xmat[i].reshape(3, 3).astype(np.float32)
            break
    ee_pose = (world_correction @ ee_pose).astype(np.float32)

    log.info(f"Google Robot (Menagerie) loaded: {len(meshes)} meshes, EE pos={ee_pose[:3, 3]}")
    return meshes, ee_pose


def _load_franka_panda() -> tuple[list, np.ndarray]:
    """Load Franka Panda + Robotiq 2F-85 gripper (DROID variant).

    Uses the composite model built from MuJoCo Menagerie
    (``franka_emika_panda`` arm + ``robotiq_2f85_v4`` gripper).
    """
    import mujoco

    mjcf_dir = _ensure_robot_assets("droid_franka_robotiq")
    mjcf_path = mjcf_dir / "panda_updated_robotiq_2f85.xml"
    model = mujoco.MjModel.from_xml_path(str(mjcf_path))
    data = mujoco.MjData(model)

    # Franka home configuration
    home_qpos = np.array([0.0, -0.78, 0.0, -2.36, 0.0, 1.57, 0.78])
    data.qpos[: len(home_qpos)] = home_qpos
    mujoco.mj_forward(model, data)

    meshes = _extract_mujoco_meshes(model, data)

    # EE pose: use the "gripper" site (on the Robotiq base), or fall
    # back to "base_mount" / "attachment" body if site isn't found.
    ee_pose = np.eye(4, dtype=np.float32)
    for si in range(model.nsite):
        name = mujoco.mj_id2name(model, mujoco.mjtObj.mjOBJ_SITE, si) or ""
        if name == "gripper":
            ee_pose[:3, 3] = data.site_xpos[si].astype(np.float32)
            ee_pose[:3, :3] = data.site_xmat[si].reshape(3, 3).astype(np.float32)
            break
    else:
        # Fallback to body search
        for i in range(model.nbody):
            name = mujoco.mj_id2name(model, mujoco.mjtObj.mjOBJ_BODY, i) or ""
            if name in ("base_mount", "attachment"):
                ee_pose[:3, 3] = data.xpos[i].astype(np.float32)
                ee_pose[:3, :3] = data.xmat[i].reshape(3, 3).astype(np.float32)
                break

    log.info(f"Franka Panda + Robotiq loaded: {len(meshes)} meshes, EE pos={ee_pose[:3, 3]}")
    return meshes, ee_pose


